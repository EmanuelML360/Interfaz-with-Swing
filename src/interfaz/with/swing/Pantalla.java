    public void ordenarPorSeleccion(int[] vector) {
        for (int i = 0; i < vector.length - 1; i++) {
            int minimo = i;
            for (int j = i + 1; j < vector.length; j++) {
                if (vector[j] < vector[minimo]) {
                    minimo = j;
                }
            }
            int aux = vector[i];
            vector[i] = vector[minimo];
            vector[minimo] = aux;
        }
    }
    
    
