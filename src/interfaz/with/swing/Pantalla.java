    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            int numero = Integer.parseInt(jTextField1.getText());
            if(1 <= numero && numero <= 100){
            if (contador <10){
                numeros[contador] = numero;
                contador++;
                JOptionPane.showMessageDialog(this, "Numero ingresado correctamente");
            }else{
                JOptionPane.showMessageDialog(this, "No se puede ingresar más números. El vector está lleno.", "Advertencia", JOptionPane.WARNING_MESSAGE);    
            }
            }else{
                JOptionPane.showMessageDialog(this, "El número ingresado no es válido. Por favor, ingrese un número entre 1 y 100.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            jTextField1.setText("");
        }catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "El número ingresado no es válido. Por favor, ingrese un número entero.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

